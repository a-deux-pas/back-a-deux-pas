package adeuxpas.back.enums;

/**
 * Enumeration representing the roles of a user in the system.
 * <p>
 * This enum defines different roles that a user can have within the application.
 * It encapsulates the possible roles such as regular user or administrator.
 * </p>
 *
 * @author Mircea Bardan
 */
public enum UserRole {
    /**
     * Represents a regular user role.
     */
    USER,

    /**
     * Represents an administrator role with elevated privileges.
     */
    ADMIN
}

